package modelo;

public class Empleado {
    private String code;
    private String nome;
    private String ape;
    private int dni;
    private int tel;
    private String core;
    private String care;
    private String ture;
    private String usre;
    private String cone;
    private String role;
    private String este;

    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getApe() {
        return ape;
    }
    public void setApe(String ape) {
        this.ape = ape;
    }
    public int getDni() {
        return dni;
    }
    public void setDni(int dni) {
        this.dni = dni;
    }
    public int getTel() {
        return tel;
    }
    public void setTel(int tel) {
        this.tel = tel;
    }
    public String getCore() {
        return core;
    }
    public void setCore(String core) {
        this.core = core;
    }
    public String getCare() {
        return care;
    }
    public void setCare(String care) {
        this.care = care;
    }
    public String getTure() {
        return ture;
    }
    public void setTure(String ture) {
        this.ture = ture;
    }
    public String getUsre() {
        return usre;
    }
    public void setUsre(String usre) {
        this.usre = usre;
    }
    public String getCone() {
        return cone;
    }
    public void setCone(String cone) {
        this.cone = cone;
    }
    public String getRole() {
        return role;
    }
    public void setRole(String role) {
        this.role = role;
    }
    public String getEste() {
        return este;
    }
    public void setEste(String este) {
        this.este = este;
    }
    
}
