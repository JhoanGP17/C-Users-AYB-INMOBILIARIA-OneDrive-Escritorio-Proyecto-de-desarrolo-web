package modelo;

public class Mesa {
    private String codm;
    private String num;
    private String piso;

    public String getCodm() {
        return codm;
    }
    public void setCodm(String codm) {
        this.codm = codm;
    }
    public String getNum() {
        return num;
    }
    public void setNum(String num) {
        this.num = num;
    }
    public String getPiso() {
        return piso;
    }
    public void setPiso(String piso) {
        this.piso = piso;
    }    
    
}
